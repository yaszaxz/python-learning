#Primeiro definir uma variável com um ponto inicial
numero = 10
#while repete um comando enquanto uma condição é verdadeira
#10 >= 1, logo, ele vai repetir esse comando enquando a variável 'número' for >= 1
#Como eu quero exibir os números de 10 a 1, a condição deve ser >= 1 e ir subtraindo 1 do número a cada repetição
while numero >= 1: 
    print(numero)
    numero -= 1